public class User {
    private String username; // 사용자 아이디
    private String password; // 사용자 암호

    public User(String username, String password) {
        this.username = username;
        this.password = password;
    }

    public String getUsername() {
        return username;
    }

    public String getPassword() {
        return password;
    }
}
